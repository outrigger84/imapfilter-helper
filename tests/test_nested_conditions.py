class TestNestedConditionsEvaluation:
    """Test evaluation of rules with nested conditions."""

    def test_evaluator_accepts_nested_any_all(self):
        """Test that evaluator handles nested any/all structures."""
        # Create a nested condition: all [ FROM, any [ SUBJECT patterns ] ]
        header = {"from": "steve.gibson@metoffice.gov.uk", "subject": "Accepted:"}

        condition = {
            "all": [
                {
                    "header": "from",
                    "contains": "steve.gibson@metoffice.gov.uk",
                },
                {
                    "any": [
                        {"header": "subject", "contains": "Accepted:"},
                        {"header": "subject", "contains": "Tentative:"},
                        {"header": "subject", "contains": "Declined:"},
                    ]
                },
            ]
        }

        # Should match - from matches AND (Accepted: matches in any group)
        result = _evaluate_condition_node(header, condition, {}, "")
        assert result is True

    def test_evaluator_rejects_when_from_no_match(self):
        """Test evaluation fails when required FROM condition doesn't match."""
        header = {
            "from": "wrong@example.com",
            "subject": "Accepted:",
        }

        condition = {
            "all": [
                {
                    "header": "from",
                    "contains": "steve.gibson@metoffice.gov.uk",
                },
                {
                    "any": [
                        {"header": "subject", "contains": "Accepted:"},
                        {"header": "subject", "contains": "Tentative:"},
                        {"header": "subject", "contains": "Declined:"},
                    ]
                },
            ]
        }

        # Should NOT match - from doesn't match (even though subject would)
        result = _evaluate_condition_node(header, condition, {}, "")
        assert result is False

    def test_evaluator_rejects_when_subject_no_match(self):
        """Test evaluation fails when none of the OR'd subjects match."""
        header = {
            "from": "steve.gibson@metoffice.gov.uk",
            "subject": "Other Subject",
        }

        condition = {
            "all": [
                {
                    "header": "from",
                    "contains": "steve.gibson@metoffice.gov.uk",
                },
                {
                    "any": [
                        {"header": "subject", "contains": "Accepted:"},
                        {"header": "subject", "contains": "Tentative:"},
                        {"header": "subject", "contains": "Declined:"},
                    ]
                },
            ]
        }

        # Should NOT match - subject doesn't match any in the OR group
        result = _evaluate_condition_node(header, condition, {}, "")
        assert result is False

